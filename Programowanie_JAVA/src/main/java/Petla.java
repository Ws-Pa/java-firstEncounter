public class Petla {
    public static void main(String[] args) {
        for (int i = 10; i > 0; i--) {
            System.out.println("Sekunda " + i);
        }
        int n = 10;
        while (n>0) {
            System.out.println("Sekunda " + n);
            n--;
        }
    }
}
